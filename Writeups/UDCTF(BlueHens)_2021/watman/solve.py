from Crypto.Util.number import inverse

flag = [ord("U"), *[0 for i in range(27)]]
output = [0x56, 0x9b, 0x5f, 0x83, 0xde, 0xbe, 0x45, 0x65, 0xe9, 0xb1, 0x51, 0xcf, 0x7f, 0xd5, 0x71, 0x35, 0xaa, 0x5e, 0x19, 0xda, 0xd9, 0x9e, 0x19, 0xf9, 0x48, 0xda, 0x43, 0xdb]
v8 = 128
for i in range(1, 28):
	v16 = flag[i-1] % 2
	v8 += (1 - 2 * v16)
	flag[i] = (inverse(v8, 257) * output[i]) % 257

print("".join(chr(i) for i in flag))